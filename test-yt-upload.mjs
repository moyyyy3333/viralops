import { google } from "googleapis";
import { createReadStream, writeFile } from "fs";
import { readFile } from "fs/promises";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const tokens = JSON.parse(await readFile(join(__dirname, ".youtube-tokens.json"), "utf8"));

const CLIENT_ID = "YOUTUBE_CLIENT_ID_REMOVED";
const CLIENT_SECRET = "YOUTUBE_CLIENT_SECRET_REMOVED";
const redirectUri = "http://localhost:5101/oauth2callback";

const auth = new google.auth.OAuth2(CLIENT_ID, CLIENT_SECRET, redirectUri);
auth.setCredentials(tokens);

if (Date.now() > (tokens.expiry_date || 0) - 60000) {
  console.log("Refreshing token...");
  const { credentials } = await auth.refreshAccessToken();
  await writeFile(join(__dirname, ".youtube-tokens.json"), JSON.stringify(credentials, null, 2));
  auth.setCredentials(credentials);
  console.log("Token refreshed");
}

const youtube = google.youtube({ version: "v3", auth });

const videoPath = "/tmp/test-short.mp4";
const res = await youtube.videos.insert({
  part: ["snippet", "status"],
  requestBody: {
    snippet: {
      title: "ViralOps AI Test",
      description: "Auto-generated by ViralOps AI System. Testing YouTube Shorts upload.",
      categoryId: "24",
    },
    status: {
      privacyStatus: "unlisted",
      selfDeclaredMadeForKids: false,
    },
  },
  media: {
    body: createReadStream(videoPath),
    mimeType: "video/mp4",
  },
});

console.log("Status:", res.status);
console.log("Video ID:", res.data.id);
console.log("URL:", "https://youtube.com/shorts/" + res.data.id);
