import { YouTubeAPIService } from './src/services/youtubeApi.js';
import { readdirSync } from 'fs';
import { join } from 'path';

const OUT = join(process.env.HOME || '/tmp', 'viralops-output');
const files = readdirSync(OUT).filter(f => f.endsWith('.mp4')).sort();

const latest = files[files.length - 1];
const videoPath = join(OUT, latest);

const stubChild = () => ({ info: () => {}, warn: () => {}, error: () => {}, debug: () => {}, child: () => stubChild });
const logger = { 
  info: (msg, data) => console.log('[INFO]', msg, data || ''), 
  warn: (msg, data) => console.log('[WARN]', msg, data || ''), 
  error: (msg, data) => console.log('[ERROR]', msg, data || ''), 
  debug: () => {}, 
  child: () => stubChild()
};

const yt = new YouTubeAPIService(logger);

await new Promise(r => setTimeout(r, 1000));

console.log('Uploading:', videoPath);

// Simple description - no newlines
const result = await yt.uploadShort(
  videoPath,
  'Auto-Generated Motivation Short - ViralOps AI',
  '100% auto-generated by AI. Trend research to FFmpeg to YouTube. No humans involved. #shorts #motivation #viral #automation',
  ['shorts', 'motivation', 'viral', 'automation', 'ai']
);

console.log('======== UPLOAD RESULT ========');
console.log(JSON.stringify(result, null, 2));
if (result.videoId) {
  console.log('✅ URL:', result.url);
} else {
  console.log('❌ UPLOAD FAILED');
}
